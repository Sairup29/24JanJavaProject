package loops;

public class ForLoop {

    public static void main(String[] args) {

        for(int a=1; a<=5; a++)
        {
            System.out.println("welcome");
        }

        System.out.println("=================================");


        for(int a=10; a<=100; a=a+10)
       {
           System.out.println(a);
       }

        System.out.println("=================================");

       for(int a=100; a>=10; a=a-10)
       {
           System.out.println(a);
       }



    }
}
