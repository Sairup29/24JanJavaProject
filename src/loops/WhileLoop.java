package loops;

public class WhileLoop {

    public static void main(String[] args) {

        int a=1;

        while(a<=5)
        {
            System.out.println("velocity");
            a++;
        }

        System.out.println("===========================");


        int b=7;
        while(b<=70)
        {
            System.out.println(b);
            b=b+7;
        }

        System.out.println("===========================");

        int c=70;
        while(c>=7)
        {
            System.out.println(c);
            c=c-7;
        }



    }
}
